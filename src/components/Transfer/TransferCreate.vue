export function validName(name) {
  let validNamePattern = new RegExp("^[a-zA-Z]+(?:[-'\\s][a-zA-Z]+)*$");
  if (validNamePattern.test(name)){
    return true;
  }
  return false;
}
export default {
    name: 'TransferCreate',
    data() {
        return {
            v$: useValidate(),
            submitting: false,
            fromTitle: "project_from",            
            FormData: {
                order:{                        
                        title: "",
                        odd_number: "",
                        transfer_date:  new Date().toISOString().slice(0,10),  
                        remark: "",                        
                        picture: null,
                        appendix: null,
                        approver:[],
                        cc: [],
                        creation_time: "",
                        project_from_id: "",
                        project_to_id: "", 
                        created_by_user: ""
                    },
                    details:[]               
            },
            detailCount: 1,
            pictureImages: [] ,
            appendixImages: [], 
            showapprover: [],
            showcc: [],
            material: [],
            selectedMaterial: [],

            dialogVisible: false,
            //pagination
            background: false,
            disabled: false,
            small: false,
            pageSize: 20,   
            currentPage: 1,  
            total: 0,    
