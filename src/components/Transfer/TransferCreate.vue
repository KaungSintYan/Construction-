            this.appendixImages = data;          
        },
        getApproverUser(data){
          this.showapprover = data;
        },
        getCcUser(data){
          this.showcc = data;
        },
        showMaterialDialog(){
            //alert(this.FormData.order.project_id)
            if(this.FormData.order.project_from_id != "") {
                this.getMaterial();
                this.dialogVisible=true;
            }
            else{
                toast.warning("please project name", {
                    position: toast.POSITION.TOP_CENTER,
                    autoClose: 1000,
                });
            }
          
        },
        async getMaterial(){
        let response = await authApi({
            method: 'GET',
                api: '/materialmanagement/project_inventory',
                params: {
                       project_id: this.FormData.order.project_from_id,
                       keyword: '',             
                       page: '',
                       limit: ''
                }  
        });
        this.material = response.data.data;
        this.total = response.data.total;       
       },
       
       addMaterial() { 

        this.selectedMaterial.forEach((val, index)=>{
                    let detail= {                      
                        transfer_quantity: "",  
                        price_per_unit: "",        
                        tax_rate: "",
                        tax: "",
                        amount: "",  
                        amt_without_tax: "", 
                        remark: "",                        
                        otherproperties: "",
                        transfer_order_id: "",                        
                        material_id: val.material_id,
                        material_name: val.material_name,
                        material_specification: val.specification,
                        material_classification: val.classification,
                        material_brand: val.brand,
                        material_unit: val.measure_unit,
                        instock_amount: val.instock_amount,
                    }   
                    let material = this.FormData.details.find((x=> x.material_id == val.inventory_id));
                    if(material == undefined){
                        this.FormData.details.push(detail);  
                    }  
        });        
           this.dialogVisible = false; 
        },
        deleteMaterial(id){
            this.FormData.details =this.FormData.details.filter(x=> x.material_id != id);  
            this.selectedMaterial.forEach((row) => { 
                if(row.inventoryid == id){
                    this.$refs.multipleTableRef.toggleRowSelection(row, undefined);                
                }
            });  
        },

        async createData(e){  

            this.submitting = true; 
                this.v$.$validate(); 
                if (this.v$.$error) {
                    this.submitting = false;
                    return;
                }
            let plan= this.FormData.details;
            if(plan.length > 0){
                for(let i=0; i< plan.length; i++){
                    if(plan[i].transfer_quantity == "" || plan[i].transfer_quantity > plan[i].instock_amount){
                        toast.warning(this.$t('num_of_plants')+ " is less than "+ plan[i].instock_amount, {
                            position: toast.POSITION.TOP_CENTER,
                            autoClose: 1000,
                        });
                        this.submitting = false;
                        return;
                    }                   
                    if(plan[i].price_per_unit == ""){
                        toast.warning(this.$t('is_required', {'name': this.$t('price_per_unit')}), {
                            position: toast.POSITION.TOP_CENTER,
                            autoClose: 1000,
                        });
                        this.submitting = false;
                        return;
                    }
                    // if(plan[i].tax_rate == ""){
                    //     toast.warning(this.$t('is_required', {'name': this.$t('tax_rate')}), {
                    //         position: toast.POSITION.TOP_CENTER,
                    //         autoClose: 1000,
                    //     });
                    //     this.submitting = false;
                    //     return;
                    // }
               }
            }     
            
            this.showapprover.forEach((value) => {
                this.FormData.order.approver.push(value.userid);                
            }); 
            this.showcc.forEach((value) => {
                this.FormData.order.cc.push(value.userid); 
            }); 
            let response = await authApi({
                method: 'POST',
                api: '/materialmanagement/transfer_order',
                data: this.FormData
            });
           
            if(response.status == 201){              
              //console.log(response.data.revenue_contract_id);              
                const id = response.data.transfer_order_id; 
                const imgformData = new FormData();
                imgformData.append('transfer_order_id',id);
                this.pictureImages.forEach((value) => {
                     imgformData.append('picture', value.url);   
                });
                this.appendixImages.forEach((value) => {
                    imgformData.append('appendix', value.url);  
                });    
                
                let imgResponse = await authApi({
                method: 'POST',
                api:  '/materialmanagement/transfer_order_datafiles',           
                data: imgformData
                });
                if(imgResponse.status == 200){
                  
                    toast.success("Requisiton Insert successful !", {
                    position: toast.POSITION.TOP_RIGHT,
                    autoClose: 3000,
                    });
                    this.$router.back();
                }
            }
        },
    }
    
}
